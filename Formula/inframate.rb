  def install
    venv = virtualenv_create(libexec, "python3.13")
    resources.each do |r|
      r.stage do
        whl = Dir.glob("*.whl").first
        if whl
          system libexec/"bin/python", "-m", "pip", "install", "-v",
                 "--no-deps", "--ignore-installed", whl
        else
          venv.pip_install Pathname.pwd
        end
      end
    end
    venv.pip_install_and_link buildpath
  end

